	<div class="sched-faq sched-faq-first">
		<div class="sched-faq-title"><i class="fa fa-plus-circle"></i>How do I create a Timetable?</div>
		<div class="sched-faq-content">
			<ol>
				<li>Go to <strong>Timetable &raquo; Timetables</strong> and click <strong>+ Create Timetable</strong>.</li>
				<li>Click on <strong>Edit Timetable</strong> on the Timetable you just created, the editor will now open.</li>
				<li>In the editor you can add Events and Columns. Change the Timetable settings by clicking <strong>Timetable Settings</strong>, and manage your </strong>Custom Fields</strong></li>
				<li>Drag columns left and right to change position. Drag custom fields up and down to change the order.</li>
			</ol>
		</div>
	</div>

	<div class="sched-faq">
		<div class="sched-faq-title"><i class="fa fa-plus-circle"></i>How do I put a Timetable shortcode onto my page?</div>
		<div class="sched-faq-content">
			<ol>
				<li>Start by creating your Timetable (view previous FAQ)</li>
				<li>Open your page or post, and click the <span class="dashicons-before dashicons-schedule"></span> button in the editor to open the shortcode generator.</li>
				<li>Click <strong>+ Insert</strong> on a timetable to insert the shortcode into your page.</li>
				<li>Additionally you can add Shortcode Options by selecting various options in the shortcode generator.</li>
			</ol>
		</div>
	</div>

	<div class="sched-faq">
		<div class="sched-faq-title"><i class="fa fa-plus-circle"></i>How do I place a Timetable widget onto my page?</div>
		<div class="sched-faq-content">
			<ol>
				<li>Start by creating your Timetable</li>
				<li>Go to <strong>Appearance &raquo; Widgets</strong>.</li>
				<li>Drag the <strong>Timetable Widget</strong> into a Widget location.</li>
				<li>Optionally add a title, and select the Timetable you want to show. Then press <strong>Save</strong>.</li>
				<li>Additionally you can add options in the custom options field. View the documentation for a complete list of options.</li>
			</ol>
		</div>
	</div>

	<div class="sched-faq">
		<div class="sched-faq-title"><i class="fa fa-plus-circle"></i>How do I install the Horizontal View Addon?</div>
		<div class="sched-faq-content">
			<ol>
				<li><a href="http://rikdevos.com/demos/wordpress-responsive-timetable/horizontal-addon/">Purchase the addon</a>, and download the zip.</li>
				<li>
					The addon plugin can be installed in two different ways:
				<ol style="margin-left: 20px;">
					
					<li>Upload the downloaded.zip file via the Wordpress Admin Panel: to do this, go to “Plugins” » “Add New”. On this page select “Upload”, then “Choose File” and locate the .zip file on your computer. Then hit the “Install Now” button and let Wordpress do its magic.</li>
					<li>Upload the extracted .zip folder and its contents to the /wp- content/plugins/ directory using an FTP client.</li>
				</ol></li>
				<li>Once you have uploaded the plugin you can activate it. Do this by going to “Plugins”. Next scroll down to “Responsive Timetable for Wordpress - Horizontal Addon” and click on Activate.</li>
				<li>The addon in now installed and ready to be used.</li>
			</ol>
		</div>
